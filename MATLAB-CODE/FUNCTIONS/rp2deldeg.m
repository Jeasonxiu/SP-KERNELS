function [dd] = rp2deldeg(rp)
%an empirical function that maps ray param to deldeg
%13.958 50
%13.418 55
%12.862 60
%12.299 65
%11.728 70
%11.139 75
%10.536 80
%9.913  85

%13.754 50
%13.222 55
%12.676 60
%12.120 65
%11.558 70
%10.973 75
%10.393 80
%9.772  85

%rp=0.11933;

%Y=[13.958, 13.418, 12.862, 12.299, 11.728, 11.139, 10.536, 9.913]./111.11;
%Y=[13.754, 13.222, 12.676, 12.120, 11.558, 10.973, 10.393, 9.772]./111.11;
%X=[50, 55, 60, 65, 70, 75, 80, 85];
%G=zeros(length(Y),3);
%G(:,1)=1;
%G(:,2)=Y;
%G(:,3)=Y.^2;
%m=X/G';

%Xhat=G*m';

%An empirical function to get deldeg from the ray param (s/km) for
%teleseismic S waves
dd= 148.2989 - 535.6816 * rp - 2087.8 * rp.^2;

end